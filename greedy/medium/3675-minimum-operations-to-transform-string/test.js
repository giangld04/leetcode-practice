const { minOperations } = require("./solution");

let passed = 0, failed = 0;
function test(actual, expected, label) {
  const a = JSON.stringify(actual);
  const e = JSON.stringify(expected);
  if (a === e) {
    console.log(`  ✓ ${label}`);
    passed++;
  } else {
    console.log(`  ✗ ${label}`);
    console.log(`    Expected: ${e}`);
    console.log(`    Actual:   ${a}`);
    failed++;
  }
}

console.log("\n3675. Minimum Operations to Transform String\n");

test(minOperations("yz"), 2, 'Example 1: minOperations("yz") → 2');
test(minOperations("a"), 0, 'Example 2: minOperations("a") → 0');

